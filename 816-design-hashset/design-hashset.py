class MyHashSet:

    def __init__(self):
        self.set = defaultdict(bool)

    def add(self, key: int) -> None:
        self.set[key] = True

    def remove(self, key: int) -> None:
        self.set[key] = False

    def contains(self, key: int) -> bool:
        return True if self.set[key] == True else False
        


